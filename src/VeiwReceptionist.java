    private void txttableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txttableMouseClicked
         int row=txttable.getSelectedRow();
        Object o=txttable.getValueAt(row,0);//0 is the coloumn no
        String s=o.toString();
        int i=Integer.parseInt(s);
        try
        {
          rs=st.executeQuery("select * from addreceptionist where id="+i);
          rs.next();
                txtid.setText(""+rs.getInt("id"));
                  txtname.setText(rs.getString("name"));
                     txtemail.setText(rs.getString("email"));
                          txtpassword.setText(rs.getString("password"));
                              txtcontact.setText(""+rs.getInt("contact"));
                                    txtaddress.setText(rs.getString("address"));
                                          
        }
        catch(SQLException sql)
        {
          JOptionPane.showMessageDialog(null,"mouse clicked error"+sql);
        }
    }//GEN-LAST:event_txttableMouseClicked

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
       String obj=txtid.getText();
       int i=Integer.parseInt(obj);
       String n=txtname.getText();
       String eml=txtemail.getText();
       String pass=txtpassword.getText();
      String obj1= txtcontact.getText();
      int cont=Integer.parseInt(obj1);
      String add=txtaddress.getText();
      try{
        pst=con.prepareStatement("update addreceptionist set name=?,email=?,password=?,contact=?,address=? where id=?");
         
          pst.setString(1,n);
          pst.setString(2,eml);
          pst.setString(3,pass);
          pst.setInt(4,cont);
          pst.setString(5,add);
          pst.setInt(6, i);
          pst.execute();
          pst.close();
          JOptionPane.showMessageDialog(null,"data updated");
          
      }
      catch(SQLException sql)
      {
          JOptionPane.showMessageDialog(null,"not updated");
      }
       
    }//GEN-LAST:event_btnupdateActionPerformed

